////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
  //Base
  if(n === 0){
    return true;
  }
  if (n === 1){
    return false;
  }
  //Recursion
  //n is positive
  if(n > 0){
    return isEven(n-2);
  }
  //n is negative
  if (n<0){
    return isEven(n+2);
  }

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char, output = 0) {
  //BASE - end when string is empty
  if (string.length === 0){
    return output;
  }

  //Recursion
  if(string[0] === char){
    output++;
  }
  return countChars(string.slice(1), char, output);
};

